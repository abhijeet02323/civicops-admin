interface MenuItemProps {
  label: string
  to: string
  icon: ReactElement
  selected: boolean
}

const CustomMenuItem = ({ label, to, icon, selected }: MenuItemProps): ReactElement => {
  return (
    <MenuItem
      component={<Link to={to} />}
      icon={icon}
      active={selected}
      className={`${
        selected ? 'bg-indigo-600 text-white' : 'text-gray-300 hover:bg-indigo-500/10'
      } transition-colors rounded-lg my-1`}
    >
      {label}
    </MenuItem>
  )
}

